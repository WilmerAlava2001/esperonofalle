base = int(input('Ingrese la base del triángulo : \t'))
altura = int(input('Ingrese la altura del triángulo : \t'))

area = (base*altura)/2
print(f'El area del triangulo es : {area}')

lado1 = int(input('Ingrese el lado 1 del triangulo : \t'))
lado2 = int(input('Ingrese el lado 2 del triangulo : \t'))
lado3 = int(input('Ingrese el lado 3 del triangulo : \t'))

perimetro = lado1+lado2+lado3
print(f'El perimetro del triangulo es : {perimetro}')
